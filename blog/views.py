def _build_calendar_weeks(selected_month, selected_year, today):
    """Build a 6-week calendar matrix and attach posts to each day."""
    calendar_matrix = calendar.Calendar(firstweekday=0).monthdatescalendar(selected_year, selected_month)
    while len(calendar_matrix) < 6:
        previous_week = calendar_matrix[-1]
        first_day = previous_week[-1] + timedelta(days=1)
        calendar_matrix.append([first_day + timedelta(days=offset) for offset in range(7)])

    dated_posts = Post.objects.exclude(publish_date__isnull=True).order_by("-publish_date", "-id")
    posts_by_date = {}
    for post in dated_posts:
        posts_by_date.setdefault(post.publish_date, []).append(post)

    calendar_weeks = []
    for week in calendar_matrix:
        week_days = []
        for day in week:
            week_days.append(
                {
                    "date": day,
                    "is_current_month": day.month == selected_month,
                    "is_today": day == today,
                    "posts": posts_by_date.get(day, []),
                }
            )
        calendar_weeks.append(week_days)

    return calendar_weeks


