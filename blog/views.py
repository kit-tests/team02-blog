@login_required
@permission_required("blog.change_post", raise_exception=True)
def post_edit(request, slug):
    """Edit one post through the browser and persist the validated changes."""
    post = get_object_or_404(Post, slug=slug)
    original_title = post.title
    original_slug = post.slug

    if request.method == "POST":
        form = PostForm(request.POST, instance=post)
        if form.is_valid():
            updated_post = save_post_from_form(
                form,
                original_title=original_title,
                original_slug=original_slug,
            )
            return redirect("post-details-page", slug=updated_post.slug)
    else:
        form = PostForm(instance=post)

    return render(request, "blog/post_edit.html", {
        "post": post,
        "form": form,
        "page_heading": "Edit post",
        "page_description": "Changes saved here are written to the local database and shown on the public post page immediately.",
        "submit_label": "Save changes",
    })


