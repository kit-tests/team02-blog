def _parse_month_year_params(request):
    """Parse and validate month/year query params with safe defaults."""
    today = timezone.localdate()
    selected_month = today.month
    selected_year = today.year

    month_query = request.GET.get("month")
    year_query = request.GET.get("year")

    try:
        if month_query is not None:
            selected_month = max(1, min(12, int(month_query)))
        if year_query is not None:
            selected_year = max(1900, min(2100, int(year_query)))
    except (TypeError, ValueError):
        selected_month = today.month
        selected_year = today.year

    return selected_month, selected_year, today


