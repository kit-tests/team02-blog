def global_chat_messages(request):
    """Return the most recent global chat messages for the widget bootstrap."""
    messages = (
        GlobalChatMessage.objects.select_related("user")
        .order_by("-created_at", "-id")[:50]
    )
    serialized = [
        {
            "username": row.user.username,
            "message": row.message,
            "created_at": timezone.localtime(row.created_at).strftime("%Y-%m-%d %H:%M:%S"),
        }
        for row in reversed(list(messages))
    ]
    return JsonResponse({"messages": serialized})


