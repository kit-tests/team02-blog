    """Create one post from the browser and persist it to the local database."""
    if request.method == "POST":
        form = PostForm(request.POST)
        if form.is_valid():
            post = save_post_from_form(form)
            return redirect("post-details-page", slug=post.slug)
    else:
        form = PostForm()

    return render(request, "blog/post_edit.html", {
        "form": form,
        "post": None,
        "page_heading": "Create post",
        "page_description": "Add a new blog post in the browser and save it directly into the local database.",
        "submit_label": "Create post",
    })


