def calendar_data_json(request):
    """Returns calendar data for a given month as JSON (no page reload)."""
    month, year, today = _parse_month_year_params(request)
    calendar_weeks = _build_calendar_weeks(month, year, today)

    weeks = []
    for week in calendar_weeks:
        week_days = []
        for day in week:
            day_posts = day["posts"]
            week_days.append({
                "date": day["date"].isoformat(),
                "day": day["date"].day,
                "is_current_month": day["is_current_month"],
                "is_today": day["is_today"],
                "has_posts": len(day_posts) > 0,
                "posts": [{"title": p.title, "slug": p.slug} for p in day_posts],
            })
        weeks.append(week_days)
    
    return JsonResponse({
        "month_name": calendar.month_name[month],
        "month": month,
        "year": year,
        "weeks": weeks,
    })

